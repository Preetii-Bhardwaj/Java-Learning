package Basics;
import java.util.*;
public class loop2 {
    public static void main(String args[]){
        Scanner sc= new Scanner(System.in);
        System.out.print("Enter your number:");
        int a=sc.nextInt();
        int i=0;
        while(i<a){
            System.out.println(i+ " ");
           i++;
        } 
    }
}
